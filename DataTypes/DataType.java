class DataType
{
    public static void main(String args[])
    {
        byte b = 10;
        //byte b = 130;
        System.out.println(b);

        short s = 12345;
        System.out.println(s);

        int i = 10;
        System.out.println(i);

        long l=10L;
        System.out.println(l);


        float f = 10.5678494678f;
        System.out.println(f);

        char c = 100;
        System.out.println(c);

        boolean bol = true;
        System.out.println(bol);




    }

}